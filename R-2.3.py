# komponen : file dokumen. Dokumen akan memiliki atribut _content yang akan berisi semua karakter dalam dokumen.
# Metode:
# 1. copy(): ini akan menyalin urutan karakter tertentu dan menyimpannya di beberapa atribut _clipboard
# 2. paste(): ini akan memasukkan isi _clipboard ke posisi dan rentang yang ditentukan di _content
# 3. insert(): menyisipkan karakter ke posisi tertentu dalam urutan
# 4. delete(): menghapus karakter dari _content